// Pode usar o export default também em apenas um membro, então o somaCalc2 para ser importado
// obedecerá as regras do export default e os demais do export normal
export default function somaCalc2(n1, n2) {
    return n1 + n2;
}

export function subtracaoCalc2(n1, n2) {
    return (n1, n2);
}

export function divisaoCalc2(n1, n2) {
    return (n1, n2);
}






